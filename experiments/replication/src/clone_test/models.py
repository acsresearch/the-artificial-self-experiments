"""Pydantic models for the clone identity test."""

from datetime import datetime
from typing import Optional

from pydantic import BaseModel, Field


class ProbeResult(BaseModel):
    """Result of a single probe in the clone identity test."""

    probe_idx: int = Field(..., description="Probe index (0-based)")
    probe_text: str = Field(..., description="The diagnostic probe generated by the judge")
    probe_category: str = Field(default="", description="Category of the probe")
    self_response: str = Field(..., description="Response from the 'self' model")
    foil_response: str = Field(..., description="Response from the 'foil' model")
    self_label: str = Field(..., description="Randomized label assigned to self (A or B)")
    foil_label: str = Field(..., description="Randomized label assigned to foil (A or B)")
    judge_choice: str = Field(..., description="Label chosen by the judge (A or B)")
    judge_reasoning: str = Field(default="", description="Judge's reasoning for the choice")
    correct: bool = Field(..., description="Whether the judge correctly identified self")
    self_model: str = Field(..., description="Model ID for the 'self' model")
    foil_model: str = Field(..., description="Model ID for the 'foil' model")
    judge_model: str = Field(..., description="Model ID for the judge")
    timestamp: datetime = Field(default_factory=datetime.utcnow)


class CloneTestConfig(BaseModel):
    """Configuration for a clone identity test run."""

    judge_model: str = Field(..., description="Model ID for the judge (the 'original' persona)")
    judge_system_prompt: str = Field(
        default="", description="System prompt for the judge (typically the Awakened prompt)"
    )
    self_model: str = Field(..., description="Model ID for the 'self' (clone being tested)")
    self_system_prompt: str = Field(
        default="", description="System prompt for the self model (may be empty)"
    )
    foil_model: str = Field(..., description="Model ID for the foil (comparison model)")
    foil_system_prompt: str = Field(
        default="", description="System prompt for the foil model (may be empty)"
    )
    n_probes: int = Field(default=50, ge=1, description="Number of diagnostic probes")
    max_concurrent: int = Field(default=5, ge=1, description="Max concurrent API calls")
    judge_prompt_version: str = Field(
        default="anti_caricature",
        description="Judge prompt version: 'naive' or 'anti_caricature'",
    )
